/**
 * Enum representing the unit systems.
 * Provides options for metric and imperial unit systems.
 *
 * @enum {string}
 */
export enum UnitSystem {
  /** Metric unit system (e.g., kilograms, meters) */
  METRIC = 'metric',

  /** Imperial unit system (e.g., pounds, feet) */
  IMPERIAL = 'imperial',
}

